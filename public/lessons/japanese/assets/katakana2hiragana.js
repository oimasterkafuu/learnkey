function convert(curr) {
    if (typeof curr === 'string') {
        var result = '';
        for (let i = 0; i < curr.length; i++) {
            if (katakana2hiragana[curr[i]]) {
                result += katakana2hiragana[curr[i]];
            } else {
                result += curr[i];
            }
        }
        if (result === curr) {
            return curr;
        } else {
            console.log(curr, '=>', result);
            return [curr, result];
        }
    }

    for (let i in curr) {
        curr[i] = convert(curr[i]);
    }

    return curr;
}

